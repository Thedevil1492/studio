// This custom hook is a wrapper around useMemo that is specifically designed to
// ensure that Firestore queries and document references are memoized correctly.
// This is critical for preventing infinite render loops when using the useCollection
// and useDoc hooks.
export function useMemoFirebase<T>(factory: () => T, deps: React.DependencyList): T {
  // eslint-disable-next-line react-hooks/exhaustive-deps
  const memoized = React.useMemo(factory, deps);
  
  if (typeof memoized !== 'object' || memoized === null) {
    return memoized;
  }
  
  (memoized as T & {__memo?: boolean}).__memo = true;
  
  return memoized;
}
